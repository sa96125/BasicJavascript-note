// 풀이방법은 크게 다르지 않지만, 2가지만의 포인터를 사용하여 풀어한다.
// 새로운 tmp공간을 생성할 필요없이 사용하지 않는 공간을 재사용했다. 대박!!
function countUniqueValue(arr) {
    if (arr.length === 0) return -1;

    let i = 0;

    for (let j = 1; j < arr.length; i++) {
        if (arr[i] !== arr[j]) {
            i++;
            arr[i] = arr[j];
        }
    }
    return i + 1;
}//---------------------------------------------------------------------------------------------------->  시간복잡도 O(n)
