// 정렬되어진 배열이 존재한다.
// 중복 값을 제외하고 유니크한 숫자의 갯수를 반환하는 함수를 작성하여라.

// > [1, 1, 2, 2, 3, 4, 5, 8, 9, 9]
// < 7

function uniqueValue(arr) {
    if (arr.length === 0) return -1;

    let cnt = 1;
    let temp = 0;

    for (let i = 0; i < arr.length; i++) {
        temp = arr[i];
        if (temp !== arr[i]) {
            cnt++;
            temp = arr[i]
        }
    }
    return cnt;
}//-----------------------------------------------------------------------------------------------------> 시간복잡도 O(n)

